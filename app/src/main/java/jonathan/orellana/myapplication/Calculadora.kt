package jonathan.orellana.myapplication

class Calculadora {
    fun sumar(num1: Int, num2: Int):Int{
        val resultado1 = num1+num2

        return resultado1
    }

    fun resta(num1: Int, num2: Int):Int{
        val resultado2 = num1-num2

        return resultado2
    }

    fun multi(num1: Int, num2: Int):Int{
        val resultado3 = num1*num2

        return resultado3
    }

    fun division(num1: Int, num2: Int):Int{
        val resultado4 = num1/num2

        return resultado4
    }
}